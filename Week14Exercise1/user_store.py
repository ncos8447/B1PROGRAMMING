import json
import os

class UserStore:
    def __init__(self, file_path):
        self.file_path = file_path

    def load(self):
        if not os.path.isfile(self.file_path):
            return []
        try:
            with open(self.file_path, "r") as f:
                return json.load(f)
        except json.JSONDecodeError:
            return []

    def save(self, users):
        with open(self.file_path, "w") as f:
            json.dump(users, f)

    def find_by_id(self, user_id):
        users = self.load()
        for user in users:
            if user["id"] == user_id:
                return user
            return None

    def update_user(self, user_id, updated_data):
        users = self.load()
        for user in users:
            if user["id"] == user_id:
                user.update(updated_data)
                self.save(users)
            return True
        return False

    def get_next_id(self):
        users = self.load()
        if not users:
            return 1
        return max(user["id"] for user in users) + 1

    def delete_user(self, user_id):
        users = self.load()

        for i, user in enumerate(users):
            if user["id"] == user_id:
                users.pop(i)
                self.save(users)
            return True
        return False
